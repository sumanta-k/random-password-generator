let firstOption = document.querySelector("#show-passwd-1");
let secondOption = document.querySelector("#show-passwd-2");

function generateRandomPassword() {
    let randomPasswd = "";
    for (let i = 1; i < 11; i++) {
        randomPasswd +=
            characters[Math.floor(Math.random() * characters.length)];
    }
    return randomPasswd;
}

function showPasswd1() {
    firstOption.textContent = generateRandomPassword();
}

function showPasswd2() {
    secondOption.textContent = generateRandomPassword();
}
